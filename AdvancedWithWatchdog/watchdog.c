#include "watchdog.h"

/**********************************************************************************************************************
 * \brief: Initializes watchdog.
 *
 * \param:
 *
 * \return:
 *
 * \remarks:
 **********************************************************************************************************************/
void watchdogInit(uint32_t timeout_ms) {
    watchdog_enable(timeout_ms, true);
}

/**********************************************************************************************************************
 * \brief: Updates the watchdog.
 *
 * \param:
 *
 * \return:
 *
 * \remarks:
 **********************************************************************************************************************/
void watchdogFeed() {
    watchdog_update();
}